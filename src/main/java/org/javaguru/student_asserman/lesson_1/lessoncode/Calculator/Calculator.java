package org.javaguru.student_asserman.lesson_1.lessoncode.Calculator;

public class Calculator {
    boolean isEven(int number) {
        return number % 2 == 0;

    }

    boolean isOdd(int number) {
        return number % 2 == 1;

    }


    double findAverage(double number, double numberother) {
        return (number + numberother) / 2;

    }

    double findAverageV2(int number, int numberother) {
        return (number + numberother) / 2;

    }

}
